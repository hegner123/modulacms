func TestTemplatePath_FindsTemplateInToolsDir(t *testing.T) {
	t.Parallel()

	// templatePath searches relative to the working directory and executable.
	// When running tests from the repo root via `go test ./tools/dbgen/...`,
	// the working directory is the package directory, so "templates/entity.go.tmpl"
	// should be found.
	p := templatePath()
	if p == "" {
		t.Fatal("templatePath() returned empty string")
	}
	// The path should end with entity.go.tmpl
	if !strings.HasSuffix(p, "entity.go.tmpl") {
		t.Errorf("templatePath() = %q, want suffix %q", p, "entity.go.tmpl")
	}
}

// makeMinimalTemplate creates a simple Go template that exercises the core
// template data without requiring the full entity.go.tmpl (which depends on
// imports that only resolve in the real project).
func makeMinimalTemplate(t *testing.T) *template.Template {
	t.Helper()
	const tmplText = `// Code generated by test; DO NOT EDIT.
package db

// {{.Entity.Name}} entity
// Singular: {{.Entity.Singular}}
// Plural: {{.Entity.Plural}}
// Table: {{.Entity.TableName}}
// IDType: {{.Entity.IDType}}
{{range .Drivers}}// Driver: {{.Name}} ({{.Struct}})
{{end}}`
	tmpl, err := template.New("test.go.tmpl").Funcs(templateFuncMap()).Parse(tmplText)
	if err != nil {
		t.Fatalf("failed to parse minimal template: %v", err)
	}
	return tmpl
}

func TestTemplateExecution_MinimalEntity(t *testing.T) {
	t.Parallel()
	tmpl := makeMinimalTemplate(t)

	e := Entity{
		Name:      "TestItems",
		Singular:  "TestItem",
		Plural:    "TestItems",
		TableName: "test_items",
		IDType:    "types.TestItemID",
		IDField:   "TestItemID",
		NewIDFunc: "types.NewTestItemID()",
		Fields: []Field{
			{AppName: "TestItemID", Type: "types.TestItemID", JSONTag: "test_item_id", IsPrimaryID: true},
		},
		OutputFile: "test_item_gen.go",
	}

	data := TemplateData{
		Entity:  e,
		Drivers: DriverConfigs,
	}

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, data); err != nil {
		t.Fatalf("template execution failed: %v", err)
	}

	output := buf.String()

	// Verify key content appears in the output
	checks := []string{
		"TestItems entity",
		"Singular: TestItem",
		"Table: test_items",
		"IDType: types.TestItemID",
		"Driver: sqlite (Database)",
		"Driver: mysql (MysqlDatabase)",
		"Driver: psql (PsqlDatabase)",
	}
	for _, check := range checks {
		if !strings.Contains(output, check) {
			t.Errorf("template output missing %q\nFull output:\n%s", check, output)
		}
	}
}

func TestTemplateExecution_RealTemplate_Compiles(t *testing.T) {
	t.Parallel()

	// Load the actual template and execute it against the first entity definition
	// to verify it produces valid Go code (gofmt-parseable)
	tmplPath := templatePath()
	if _, err := os.Stat(tmplPath); err != nil {
		t.Skipf("template not found at %q (running outside repo root?): %v", tmplPath, err)
	}

	tmpl, err := template.New(filepath.Base(tmplPath)).Funcs(templateFuncMap()).ParseFiles(tmplPath)
	if err != nil {
		t.Fatalf("failed to parse template: %v", err)
	}

	// Use the first entity from Entities (Users is typically a representative one)
	if len(Entities) == 0 {
		t.Fatal("Entities slice is empty")
	}

	data := TemplateData{
		Entity:  Entities[0],
		Drivers: DriverConfigs,
	}

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, data); err != nil {
		t.Fatalf("template execution failed for entity %q: %v", Entities[0].Name, err)
	}

	// The generated output must be valid Go code
	_, err = format.Source(buf.Bytes())
	if err != nil {
		t.Errorf("generated code for entity %q is not valid Go:\n%v\nFirst 500 bytes:\n%s",
			Entities[0].Name, err, truncate(buf.String(), 500))
	}
}

